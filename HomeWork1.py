#Округление
i = float(input('Число для округления: '))
n = int(input('Количество знаков после запятой: '))
x = round(i, n)
print('Ответ:',x)

#Среднеарефмитическое
num1 = float(input('Первое число: '))
num2 = float(input('Второе число: '))
i = (num1 + num2)
x = (i / 2)
x = round(x, 0)
print('Среднеарефметическое:', x)
